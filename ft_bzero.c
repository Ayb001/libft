#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*b;

	b = (unsigned char *)s;
	while (i < n)
	{
		b[i] = 0;
		i++;
	}
	return (s);
}
